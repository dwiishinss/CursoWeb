CREATE TABLE PESSOA(
    ID BIGINT NOT NULL,
    NOME VARCHAR(50) NOT NULL,
    ATIVO SMALLINT NOT NULL,
    LOGRADOURO VARCHAR(30),
    NUMERO VARCHAR(10),
    COMPLEMENTO VARCHAR(30),
    BAIRRO VARCHAR(30),
    CEP VARCHAR(30),
    CIDADE VARCHAR(30),
    ESTADO VARCHAR(30),
    PRIMARY KEY(ID)
);

CREATE GENERATOR GEN_ID_SEQUENCE_PESSOA;

