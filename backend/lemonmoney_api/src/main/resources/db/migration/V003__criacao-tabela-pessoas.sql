CREATE TRIGGER BI_PESSOA FOR PESSOA
BEFORE INSERT
AS
BEGIN
  IF (NEW.ID IS NULL) THEN
    NEW.ID = NEXT VALUE FOR GEN_ID_SEQUENCE_PESSOA;
END
